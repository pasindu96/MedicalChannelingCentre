    private void btnUpdateUNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateUNActionPerformed
        if(Validation.validateText(txtNewUser.getText())){
            JOptionPane.showMessageDialog(null, "Please Enter the user name");
            txtNewUser.requestFocus();
            return;
        }
        if(Validation.validateText(txtPassword.getText())){
           JOptionPane.showMessageDialog(null, "Please Enter the password");
            txtNewUser.requestFocus();
            return; 
        }
        Connection conn=DBConnection.getInstance().getConnection();
        String sql="insert into login values(?,?,?);";
        try {
            PreparedStatement pstm=conn.prepareStatement(sql);
            pstm.setObject(1,null);
            pstm.setObject(2, txtNewUser.getText());
            pstm.setObject(3, txtPassword.getText());
            int affectedRows=pstm.executeUpdate();
            if(affectedRows>0)
                JOptionPane.showMessageDialog(this,"User added");
            else
                JOptionPane.showMessageDialog(this, "Error occured");
        } catch (SQLException ex) {
            Logger.getLogger(SettingsPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtNewUser.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_btnUpdateUNActionPerformed

