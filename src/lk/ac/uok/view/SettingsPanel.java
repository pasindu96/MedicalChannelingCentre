    private void btnUpdatePWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePWActionPerformed
        if(Validation.validateText(txtCurrentPW.getText())){
            JOptionPane.showMessageDialog(null, "Please Enter the current password");
            txtCurrentPW.requestFocus();
            return;
        }
        if(Validation.validateText(txtNewPW.getText())){
            JOptionPane.showMessageDialog(null, "Please Enter the new password");
            txtNewPW.requestFocus();
            return;
        }
        if(Validation.validateText(txtConfirmPW.getText())){
            JOptionPane.showMessageDialog(null, "Please Enter the new password again");
            txtConfirmPW.requestFocus();
            return;
        }
        Connection connection=DBConnection.getInstance().getConnection();
        String sql="select * from login where username=? and password=?;";
        sql = "SELECT * FROM login WHERE username='"+currentUser+"' and password='"+txtCurrentPW.getText()+"' ";
        try {
            //PreparedStatement pstm=connection.prepareStatement(sql);
            Statement stm=connection.createStatement();
            //pstm.setObject(1, currentUser);
           //pstm.setObject(2, txtCurrentPW);
            ResultSet rst=stm.executeQuery(sql);
            
            //ResultSet rst=stm.executeQuery();
            //System.out.println(rst.getObject(2));
            if(rst.next()){
                sql="update login set password=? where username=? and password=?;";
                PreparedStatement pstm=connection.prepareStatement(sql);
                pstm=connection.prepareStatement(sql); 
                pstm.setObject(1,txtNewPW.getText());
                pstm.setObject(2,currentUser);
                pstm.setObject(3,txtCurrentPW.getText());
                int affectedRows=pstm.executeUpdate();
                if(affectedRows>0)
                    JOptionPane.showMessageDialog(this, "Password Changed");
                
            }else{
                    JOptionPane.showMessageDialog(this, "Incorrect current password");
                    clearAll();
                    txtCurrentPW.requestFocus();
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(SettingsPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnUpdatePWActionPerformed

